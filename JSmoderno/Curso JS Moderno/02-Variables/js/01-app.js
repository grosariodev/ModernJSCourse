// Veremos las variables.
// Existen 3 formas de crear variables en JS, pero en las nuevas versiones solo se usan 2.


// CREAREMOS VARIABLES CON "VAR" (Ya no se usa tanto).


// Inicializar las variables con un valor.
var producto = "Monitor de 24 pulgadas";
console.log(producto);


// Las variables se pueden reasignar.
producto = "Montior de 19 pulgadas";
console.log(producto);


// JS es un lenguaje de tipo dinamico. No hay tipado.
producto = 20;
console.log(producto);



