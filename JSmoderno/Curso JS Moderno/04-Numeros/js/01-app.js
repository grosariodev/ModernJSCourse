// Como crear numeros en JS

// Este ya no es un numero, es un string que contiene un numero.
const numero1 = "20";

// Este es la forma comun de hacer un numero.
const numero2 = 20;

// En JS al no ser tipado no es necesario poner el tipo de numero que se pondra, JS interpreata o entiende a todos los numeros:
const numero3 = 2.10;
const numero4 = .20;
const numero5 = -20;