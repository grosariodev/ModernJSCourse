// Cuando se quiere poner dobles comillas ("") y no quieres que sean tomadas en cuenta, puedes esconderlas con un slash al reves \


// Esta es la primera forma de hacer un string:
const producto = "Monitor 20\"";

console.log(producto);


// Segunda forma de hacer un string:
const producto2 = String("Monitor 30 pulgadas");

console.log(producto2);


// Tercera forma de hacer un string:
const producto3 = new String("Monitor 24 pulgadas");

console.log(producto3);